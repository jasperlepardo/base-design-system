/**
 * build-components.mjs — generate component-tier CSS + TS manifest from
 * tokens/components/button.json.
 *
 * Button color tokens are keyed "intent/style" with rest/hover states; every
 * value is a semantic/raw CSS var from the token build (or `transparent`). We
 * emit:
 *   src/styles/components/button.css        base + [data-intent][data-style][data-size] + :hover/:disabled
 *   src/tokens/generated/button.manifest.ts intents/styles/sizes arrays + types
 *
 * Because every value references a themed CSS var, the Button re-themes across
 * light/dark with no extra work. Disabled is a generic opacity rule.
 *
 * Run: npm run components:build  (after the token build)
 */
import { readFileSync, writeFileSync, mkdirSync, readdirSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const read = (p) => JSON.parse(readFileSync(resolve(root, p), 'utf8'));
const button = read('tokens/components/button.json');

// --- Collect the set of CSS vars that actually exist (from generated CSS) ---
const valid = new Set();
const tokensDir = resolve(root, 'src/styles/tokens');
for (const f of readdirSync(tokensDir).filter((f) => f.endsWith('.css'))) {
  const css = readFileSync(resolve(tokensDir, f), 'utf8');
  for (const m of css.matchAll(/^\s*(--[a-z0-9-]+):/gim)) valid.add(m[1]);
}

// --- Validate every referenced var exists ---
const missing = new Set();
const isVar = (v) => typeof v === 'string' && v.startsWith('--');
const checkVar = (v) => {
  if (isVar(v) && !valid.has(v)) missing.add(v);
};
for (const combo of Object.values(button.colors))
  for (const state of Object.values(combo)) for (const v of Object.values(state)) checkVar(v);
for (const dim of Object.values(button.sizing)) for (const v of Object.values(dim)) checkVar(v);
if (missing.size) {
  console.error(`✗ button.json references ${missing.size} unknown CSS var(s):`);
  for (const m of missing) console.error('    ' + m);
  process.exit(1);
}

// --- Derive axes ---
const combos = Object.keys(button.colors); // "intent/style"
const intents = [...new Set(combos.map((c) => c.split('/')[0]))];
const styles = [...new Set(combos.map((c) => c.split('/')[1]))];
const sizes = Object.keys(button.sizing);
const ref = (v) => (isVar(v) ? `var(${v})` : v);

// --- Emit CSS ---
let css = `/* Generated by scripts/build-components.mjs — do not edit. */\n\n`;
css += `.jspr-btn {\n`;
css += `  display: inline-flex;\n  align-items: center;\n  justify-content: center;\n`;
css += `  box-sizing: border-box;\n  border: var(--border-width-default) solid var(--btn-border);\n`;
css += `  border-radius: var(--btn-radius);\n  height: var(--btn-h);\n`;
css += `  padding-inline: var(--btn-px);\n  gap: var(--btn-gap);\n`;
css += `  font-family: var(--font-family-body), system-ui, sans-serif;\n`;
css += `  font-size: var(--btn-fs);\n  line-height: var(--btn-lh);\n`;
css += `  font-weight: var(--font-weight-emphasis);\n`;
css += `  background-color: var(--btn-bg);\n  color: var(--btn-text);\n`;
css += `  cursor: pointer;\n  white-space: nowrap;\n  text-decoration: none;\n`;
css += `  transition: background-color 120ms ease, border-color 120ms ease, color 120ms ease;\n}\n\n`;
css += `.jspr-btn:focus-visible {\n  outline: 2px solid var(--border-focus);\n  outline-offset: 2px;\n}\n\n`;
css += `.jspr-btn:disabled,\n.jspr-btn[aria-disabled="true"] {\n  opacity: 0.5;\n  cursor: not-allowed;\n}\n\n`;
css += `.jspr-btn__icon { display: inline-flex; flex: none; }\n\n`;

// Sizes → CSS custom props
for (const size of sizes) {
  const s = button.sizing[size];
  css += `.jspr-btn[data-size="${size}"] {\n`;
  css += `  --btn-h: ${ref(s.height)};\n  --btn-px: ${ref(s['padding-x'])};\n`;
  css += `  --btn-gap: ${ref(s.gap)};\n  --btn-fs: ${ref(s['font-size'])};\n`;
  css += `  --btn-lh: ${ref(s['line-height'])};\n  --btn-radius: ${ref(s.radius)};\n}\n\n`;
}

// Intent/style → color custom props (rest) + :hover override
for (const combo of combos) {
  const [intent, style] = combo.split('/');
  const { rest, hover } = button.colors[combo];
  const sel = `.jspr-btn[data-intent="${intent}"][data-style="${style}"]`;
  css += `${sel} {\n  --btn-bg: ${ref(rest.bg)};\n  --btn-text: ${ref(rest.text)};\n  --btn-border: ${ref(rest.border)};\n}\n`;
  css += `${sel}:hover:not(:disabled):not([aria-disabled="true"]) {\n`;
  css += `  --btn-bg: ${ref(hover.bg)};\n  --btn-text: ${ref(hover.text)};\n  --btn-border: ${ref(hover.border)};\n}\n\n`;
}

mkdirSync(resolve(root, 'src/styles/components'), { recursive: true });
writeFileSync(resolve(root, 'src/styles/components/button.css'), css);

// --- Emit TS manifest ---
const arr = (name, values) =>
  `export const ${name} = [${values.map((v) => `'${v}'`).join(', ')}] as const;\n` +
  `export type ${name[0].toUpperCase()}${name.slice(1, -1)} = (typeof ${name})[number];\n`;

let ts = `// Generated by scripts/build-components.mjs — do not edit.\n\n`;
ts += arr('buttonIntents', intents);
ts += arr('buttonStyles', styles);
ts += arr('buttonSizes', sizes);

mkdirSync(resolve(root, 'src/tokens/generated'), { recursive: true });
writeFileSync(resolve(root, 'src/tokens/generated/button.manifest.ts'), ts);

console.log(
  `✓ button: ${intents.length} intents × ${styles.length} styles × ${sizes.length} sizes → button.css + manifest`,
);
